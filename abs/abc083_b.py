N,A,B=map(int,input().split())
S=0

for i in range(1,N+1):
    N_10000=i//10000
    X_10000=i-N_10000*10000
    N_1000=X_10000//1000
    X_1000=X_10000-N_1000*1000
    N_100=X_1000//100
    X_100=X_1000-N_100*100
    N_10=X_100//10
    X_10=X_100-N_10*10
    N_1=X_10%10
    D=N_10000+N_1000+N_100+N_10+N_1
    if D>=A and D<=B:
        S+=i
print(S)